    private void btnGuardarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTiendaActionPerformed
        Controlador.TiendaControlador objetoTienda = new Controlador.TiendaControlador();
        objetoTienda.InsertartTienda(txtNombreTienda, txtDireccionTienda);
        objetoTienda.mostrarTienda(tbTiendas);
        objetoTienda.limpiarTienda(txtIdTienda, txtNombreTienda, txtDireccionTienda);
    }//GEN-LAST:event_btnGuardarTiendaActionPerformed

    private void tbTiendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTiendasMouseClicked
        Controlador.TiendaControlador objetoTienda = new Controlador.TiendaControlador();
        objetoTienda.SeleccionarTienda(tbTiendas, txtIdTienda, txtNombreTienda, txtDireccionTienda);
    }//GEN-LAST:event_tbTiendasMouseClicked

    private void btnModificarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarTiendaActionPerformed
        Controlador.TiendaControlador objetoTienda = new Controlador.TiendaControlador();
        objetoTienda.ModificarTienda(txtIdTienda, txtNombreTienda, txtDireccionTienda);
        objetoTienda.mostrarTienda(tbTiendas);
        objetoTienda.limpiarTienda(txtIdTienda, txtNombreTienda, txtDireccionTienda);
    }//GEN-LAST:event_btnModificarTiendaActionPerformed

    private void btnEliminarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTiendaActionPerformed
        Controlador.TiendaControlador objetoTienda = new Controlador.TiendaControlador();
        objetoTienda.EliminarTienda(txtIdTienda);
        objetoTienda.mostrarTienda(tbTiendas);
        objetoTienda.limpiarTienda(txtIdTienda, txtNombreTienda, txtDireccionTienda);
    }//GEN-LAST:event_btnEliminarTiendaActionPerformed

